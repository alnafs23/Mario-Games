    /**
     * Metode act() milik World akan memeriksa Mario dan menggeser dunia.
     */
    public void act()
    {
        mario mario = getMario(); 
        if (mario == null) return; 

        int marioX = mario.getX(); 
        int scrollAmount = 0;

        // --- SCROLLING KE KANAN ---
        if (marioX > SCROLL_TRIGGER_RIGHT)
        {
            scrollAmount = marioX - SCROLL_TRIGGER_RIGHT;
            if (totalScrolled + scrollAmount > LEVEL_WIDTH - WINDOW_WIDTH)
            {
                scrollAmount = (LEVEL_WIDTH - WINDOW_WIDTH) - totalScrolled;
            }
        }
        
        
        else if (marioX < SCROLL_TRIGGER_LEFT)
        {
            scrollAmount = marioX - SCROLL_TRIGGER_LEFT; 
            if (totalScrolled + scrollAmount < 0)
            {
                scrollAmount = -totalScrolled;
            }
        }

        if (scrollAmount != 0)
        {
            scrollWorld(scrollAmount);
            totalScrolled += scrollAmount;
        }
        updateTime();
        updateHUD();
    }

    /**
     * Helper untuk menggeser SEMUA objek di dunia.
     */
    private void scrollWorld(int amount)
    {
        for (Actor actor : getObjects(Actor.class))
        {
            if (actor instanceof HUD) {
                continue; 
            }
            actor.setLocation(actor.getX() - amount, actor.getY());
        }
    }

    /**
     * Helper untuk menemukan objek Mario dengan aman.
     */
    public mario getMario()
    {
        List<mario> marios = getObjects(mario.class);
        if (marios.isEmpty()) {
            return null;
        } else {
            return marios.get(0);
        }
    }
    
    
    private void addGroundRow()
    {
       int tileWidth = 32; 
       int tileHeight = 32;

       int groundY = WINDOW_HEIGHT - tileHeight / 2;
       int groundY2 = groundY - tileHeight; 

       for (int x = 0; x < 500; x += tileWidth) {
           addObject(new Ground(), x + tileWidth / 2, groundY);
           addObject(new Ground(), x + tileWidth / 2, groundY2);
       }
    
       for (int x = 700; x < 1200; x += tileWidth) {
           addObject(new Ground(), x + tileWidth / 2, groundY);
           addObject(new Ground(), x + tileWidth / 2, groundY2);
       }

       for (int x = 1400; x < LEVEL_WIDTH; x += tileWidth) {
           addObject(new Ground(), x + tileWidth / 2, groundY);
           addObject(new Ground(), x + tileWidth / 2, groundY2);
       } 
    }
    
    
