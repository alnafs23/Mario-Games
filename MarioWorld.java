    private void updateTime()
    {
        timer++; 
        if (timer >= 60) { 
            time--;     
            timer = 0;   
        }
        
        if (time <= 0) {
            Greenfoot.stop(); 
        }
    }
    private void updateHUD()
    {
        mario mario = getMario(); 
        
        if (mario != null && hud != null)
        {
            int skor = mario.getScore();
            int koin = mario.getCoinCount();
            
            int nyawa = mario.getLives();
            hud.update(skor, koin, time, nyawa);
        }
    }
    
